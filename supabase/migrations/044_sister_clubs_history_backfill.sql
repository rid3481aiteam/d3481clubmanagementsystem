  )
) AS v(partner_name, established_date, president_name, relationship_note)
WHERE c.name = '台北市和平扶輪社'
  AND NOT EXISTS (
    SELECT 1 FROM sister_clubs sc
    WHERE sc.club_id = c.id AND sc.partner_name = v.partner_name
  );
