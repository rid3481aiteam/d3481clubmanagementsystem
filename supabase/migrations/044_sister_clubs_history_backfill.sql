-- 從社史文件（P17台北市和平扶輪社歷史軌跡.docx、P21歷屆社長及重要服務成果.docx）
-- 整理出文件裡明確提到「締結友好社／姊妹社」的對象，補進 sister_clubs 表。
--
-- 只收錄文件裡明確使用「友好社」「姊妹社」「聯誼會」等結盟字眼、且有具體對象名稱的關係；
-- 「輔導社」（台北西區社）與「本社輔導成立的新社」（太平/永平/承平/三平扶輪社）性質上是
-- 創社輔導／傳承關係，不是同等地位的友好社締結，這次沒有收錄，留給社辦自行判斷要不要另外補。
--
-- 可重複執行：用 partner_name 精準比對是否已存在（NOT EXISTS），已存在就跳過，不會造成重複列。
-- sister_clubs 表本身沒有 UNIQUE 限制，這裡改用查詢層面自己防重複。
-- ════════════════════════════════════════════

